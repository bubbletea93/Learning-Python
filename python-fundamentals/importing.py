# Importing from math library
from math import *

# Floor of a number
number = 3.98
print("floor(" + str(number) + ") = " + str(floor(number)))

# Ceil of a number
print("ceiling(" + str(number) + ") = " + str(ceil(number)))

# Square root of a number
number = 36
print("sqrt(" + str(number) + ") = " + str(sqrt(number)))